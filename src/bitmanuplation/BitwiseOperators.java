public class BitwiseOperators {
	public static void main(String[] args) {
		//And Operator.
		int iValue = 5;
		int jValue = 6;
		System.out.println(iValue & jValue);
		//Not Operator(~).
		int value = 0;
		System.out.println("Value : " + ~value);
	}
}
